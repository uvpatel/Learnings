# compare values of two operands 
# return values in boolean
x = 10
y = 20

print("x > y is", x > y)  # False greater than
print("x < y is", x < y)  # True less than
print("x == y is", x == y)  # False     equal to

print("x != y is", x != y)  # True not equal to
print("x >= y is", x >= y)  # False greater than or equal to
print("x <= y is", x <= y)  # True less than or equal to

